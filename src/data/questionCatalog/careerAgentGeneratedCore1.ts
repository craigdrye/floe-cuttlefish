// Per-distractor explanations are required — no DEFAULT_FLAW constants.
// Each wrong-answer string reflects the specific misconception a learner
// might hold. Voice template: src/data/questionCatalog/jargonBusters.ts
// and the hand-written PM/PM/UX/Leadership blocks in career.ts.
function q(
  id: number,
  topic: Topic,
  chapter: string,
  title: string,
  prompt: string,
  correct: string,
  wrong: [string, string][], // [label, whyWrong]
  lesson: string,
  extra?: {
    alternatePrompts?: Question['alternatePrompts']
    challengeRating?: Question['challengeRating']
    solution?: string
    mentorHint?: string
  },
): Question {
  return makeSimpleQuestion(
    id,
    topic,
    chapter,
    title,
    prompt,
    correct,
    wrong.map(([label, why]) => [label, why, lesson] as [string, string, string]),
    lesson,
    SOURCE,
    undefined,
    extra?.mentorHint,
    extra,
  )
}

