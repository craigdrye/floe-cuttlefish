const SOURCE = 'Floe career agent-generated climate science top-off bank'
const DEFAULT_FLAW = 'This is a plausible distractor, but it does not match the climate-science concept in the scenario.'

function q(
  id: number,
  topic: Topic,
  chapter: string,
  title: string,
  prompt: string,
  correct: string,
  wrong: string[],
  lesson: string,
): Question {
  return makeSimpleQuestion(
    id,
    topic,
    chapter,
    title,
    prompt,
    correct,
    wrong.map((label) => [label, DEFAULT_FLAW, lesson] as [string, string, string]),
    lesson,
    SOURCE,
  )
}

