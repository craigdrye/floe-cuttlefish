export function normalizeCareerCredentialChapters(catalog: Record<string, Question[]>): Record<string, Question[]> {
  return Object.fromEntries(
    Object.entries(catalog).map(([trackId, questions]) => {
      const rules = chapterRules[trackId]
      if (!rules) return [trackId, questions]

      return [trackId, questions.map((question) => normalizeQuestionChapter(question, rules, fallbackChapters[trackId]))]
    }),
  )
}

function normalizeQuestionChapter(
  question: Question,
  rules: Array<[RegExp, string]>,
  fallbackChapter: string,
): Question {
  const originalChapter = question.chapter
  if (originalChapter === fallbackChapter) return question
  if (rules.some(([, chapter]) => chapter === originalChapter)) return question

  const normalizedChapter = rules.find(([pattern]) => pattern.test(originalChapter))?.[1] ?? fallbackChapter
  if (normalizedChapter === originalChapter) return question

  return {
    ...question,
    chapter: normalizedChapter,
    subTopic: question.subTopic ?? originalChapter,
  }
}
