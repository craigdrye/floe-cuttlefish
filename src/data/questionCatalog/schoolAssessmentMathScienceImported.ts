const miss = (answer: string, why: string, hint: string): [string, string, string] => [answer, why, hint]

type ImportedAssessmentQuestion = {
  id: number
  chapter: string
  title: string
  prompt: string
  correct: string
  wrong: [string, string, string][]
  sourceId: string
  sourceName: string
  sourceUrl?: string
}

const q = ({
  id,
  chapter,
  title,
  prompt,
  correct,
  wrong,
  sourceId,
  sourceName,
  sourceUrl,
}: ImportedAssessmentQuestion) => ({
  id,
  chapter,
  title,
  prompt,
  correct,
  wrong,
  lesson: `Converted from ${sourceName} raw item ${sourceId}. ${sourceUrl ? `Source URL: ${sourceUrl}. ` : ''}The extracted row had a usable prompt, answer key, and complete fixed-choice distractors; explanations were authored during Floe import.`,
  source: `${sourceName} (${sourceId})`,
})

