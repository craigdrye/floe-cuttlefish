const SOURCE = 'Floe interview reasoning polish'
const DEFAULT_FLAW = 'This answer may sound quick, but it skips the disciplined reasoning the interview is testing.'

function q(
  id: number,
  topic: Topic,
  chapter: string,
  title: string,
  prompt: string,
  correct: string,
  wrong: string[],
  lesson: string,
): Question {
  return makeSimpleQuestion(
    id,
    topic,
    chapter,
    title,
    prompt,
    correct,
    wrong.map((label) => [label, DEFAULT_FLAW, lesson] as [string, string, string]),
    lesson,
    SOURCE,
  )
}

