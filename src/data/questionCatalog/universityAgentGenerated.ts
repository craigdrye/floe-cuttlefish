const SOURCE = 'Floe university agent-generated syllabus bank'
const DEFAULT_FLAW = 'This is a plausible distractor, but it does not match the scenario or syllabus concept.'

function q(
  id: number,
  topic: Topic,
  chapter: string,
  title: string,
  prompt: string,
  correct: string,
  wrong: string[],
  lesson: string,
): Question {
  return makeSimpleQuestion(
    id,
    topic,
    chapter,
    title,
    prompt,
    correct,
    wrong.map((label) => [label, DEFAULT_FLAW, lesson] as [string, string, string]),
    lesson,
    SOURCE,
  )
}

