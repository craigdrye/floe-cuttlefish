export function topUpUniversityAgentGeneratedTrack(trackId: string, existing: Question[], minimum = 50): Question[] {
  const generated = universityAgentGeneratedQuestionsByTrack[trackId] ?? []
  if (existing.length >= minimum || generated.length === 0) return existing

  const existingIds = new Set(existing.map((question) => question.id))
  const existingPrompts = new Set(existing.map(promptKey))
  const additions: Question[] = []

  for (const question of generated) {
    const key = promptKey(question)
    if (existingIds.has(question.id) || existingPrompts.has(key)) continue
    existingIds.add(question.id)
    existingPrompts.add(key)
    additions.push(question)
    if (existing.length + additions.length >= minimum) break
  }

  return [...existing, ...additions]
}

function promptKey(question: Question) {
  return question.prompt
    .toLowerCase()
    .replace(/[“”]/g, '"')
    .replace(/[‘’]/g, "'")
    .replace(/[^a-z0-9]+/g, ' ')
    .replace(/\s+/g, ' ')
    .trim()
}

export function buildUniversityAgentGeneratedQuestionCatalog(): Record<string, Question[]> {
  return universityAgentGeneratedQuestionsByTrack
}
